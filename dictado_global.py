class AudioRecorder:
    """Gestiona la grabaci√≥n de audio del micr√≥fono."""
    
    def __init__(self, sample_rate: int = SAMPLE_RATE, channels: int = CHANNELS):
        import sounddevice as sd
        import numpy as np
        
        self.sample_rate = sample_rate
        self.channels = channels
        self.recording = False
        self.audio_data = []
        self.stream: Optional[sd.InputStream] = None
        self._lock = threading.Lock()
        self.sd = sd
        self.np = np
    
    def _audio_callback(self, indata, frames, time_info, status):
        """Callback que se ejecuta para cada bloque de audio."""
        if status:
            print(f"‚ö†Ô∏è  Estado del stream: {status}")
        
        with self._lock:
            if self.recording:
                self.audio_data.append(indata.copy())
    
    def start_recording(self):
        """Inicia la grabaci√≥n de audio."""
        with self._lock:
            self.audio_data = []
            self.recording = True
        
        self.stream = self.sd.InputStream(
            samplerate=self.sample_rate,
            channels=self.channels,
            dtype=DTYPE,
            callback=self._audio_callback,
            blocksize=1024
        )
        self.stream.start()
        print("üéôÔ∏è  Grabando... (mant√©n pulsadas las teclas)")
    
    def stop_recording(self) -> Optional[str]:
        """
        Detiene la grabaci√≥n y guarda el audio en un archivo temporal.
        
        Returns:
            Ruta al archivo .wav temporal, o None si no hay audio.
        """
        with self._lock:
            self.recording = False
        
        if self.stream:
            self.stream.stop()
            self.stream.close()
            self.stream = None
        
        with self._lock:
            if not self.audio_data:
                print("‚ö†Ô∏è  No se captur√≥ audio.")
                return None
            
            # Concatenar todos los bloques de audio
            audio_array = self.np.concatenate(self.audio_data, axis=0)
        
        # Guardar en archivo temporal
        return self._save_to_wav(audio_array)
    
    def _save_to_wav(self, audio_array) -> str:
        """Guarda el array de audio en un archivo WAV temporal."""
        from scipy.io import wavfile
        
        # Crear archivo temporal
        temp_file = tempfile.NamedTemporaryFile(
            suffix=".wav",
            delete=False,
            prefix="dictado_"
        )
        temp_path = temp_file.name
        temp_file.close()
        
        # Escribir el archivo WAV
        wavfile.write(temp_path, self.sample_rate, audio_array)
        
        duration = len(audio_array) / self.sample_rate
        print(f"‚úÖ Audio guardado: {duration:.1f}s")
        
        return temp_path


def grabar_audio() -> Optional[str]:
    """
    Funci√≥n de conveniencia para grabar audio push-to-talk.
    
    Returns:
        Ruta al archivo .wav temporal con el audio grabado, o None si falla.
    """
    recorder = AudioRecorder()
    recorder.start_recording()
    return recorder


